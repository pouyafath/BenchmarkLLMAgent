"""
Evaluate solver predictions against ground truth using run_instance.py

Usage:
    python scripts/evaluate/run_evaluation_pipeline.py
"""

import json
import os
import subprocess
import sys
from pathlib import Path

_root = Path(__file__).resolve().parent.parent.parent
sys.path.insert(0, str(_root))

PREDICTIONS_DIR = _root / "results" / "solving_after_enhancement"
EVAL_RESULTS_DIR = _root / "eval_results"
SAMPLES_JSON = _root / "data" / "samples" / "swe_bench_live_10_samples.json"

def main():
    if not PREDICTIONS_DIR.exists():
        print(f"Predictions dir not found: {PREDICTIONS_DIR}")
        sys.exit(1)
        
    EVAL_RESULTS_DIR.mkdir(parents=True, exist_ok=True)
    
    # Load samples to match the repo and base_commit mapped to instance_ids
    with open(SAMPLES_JSON) as f:
        samples_data = json.load(f)["issues"]
        
    instance_lookup = {}
    for issue in samples_data:
        # e.g., aws-cloudformation/cfn-lint #3764
        # map back to "aws-cloudformation__cfn-lint" and "3764"
        repo = f"{issue['pr_owner']}__{issue['pr_repo']}"
        num = str(issue["issue_number"])
        instance_lookup[(repo, num)] = issue["_swe_live_instance_id"]
        
    prediction_files = sorted(list(PREDICTIONS_DIR.glob("*.json")))
    print(f"Found {len(prediction_files)} predictions to evaluate.")
    
    # Process each prediction
    for pred_file in prediction_files:
        with open(pred_file) as f:
            pred_data = json.load(f)
            
        """
        Filename format:
        openai_agents_sdk_after_enhancement__live_swe_agent__instructlab__instructlab__3136.json
        parts: 
        0: openai_agents_sdk_after_enhancement
        1: live_swe_agent  (agent_id)
        2: instructlab     (owner)
        3: instructlab     (repo)
        4: 3136             (issue number)
        """
        parts = pred_file.stem.split("__")
        if len(parts) != 5:
            continue
            
        solver_id = parts[0]
        enhancer_id = parts[1]
        owner = parts[2]
        repo_name = parts[3]
        issue_num = parts[4]
        
        repo_key = f"{owner}__{repo_name}"
        
        # We need the full swe-bench-live instance ID to fetch the instance JSON from huggingface or locally
        # The python script actually uses the json that is given to it via --dataset_json
        gt_filename = f"{repo_key}__{issue_num}.json"
        gt_path = _root / "data" / "ground_truth_swe_bench_live" / gt_filename
        
        if not gt_path.exists():
            print(f"Skipping {pred_file.name}, missing GT {gt_filename}")
            continue
            
        # Write the patch to a temp file
        patch_text = pred_data.get("patch", "")
        # If no patch, consider it 0
        if not patch_text:
            print(f"Skipping {pred_file.name}, no patch generated by agent.")
            continue
            
        out_patch_path = EVAL_RESULTS_DIR / f"{pred_file.stem}_patch.diff"
        with open(out_patch_path, "w") as f:
            f.write(patch_text)
            
        # The evaluation script outputs to out_dir/instance_id
        # We'll pass instance_id as the full prediction name so it isolates it
        instance_id = pred_file.stem
        
        # Run run_instance.py
        cmd = [
            sys.executable,
            str(_root / "scripts" / "evaluate" / "run_instance.py"),
            "--dataset_json", str(gt_path),
            "--instance_id", instance_id, # run_instance.py doesn't actually filter by this if it's not a list
            "--patch_file", str(out_patch_path),
            "--out_dir", str(EVAL_RESULTS_DIR),
            "--issue_context", f"Enhancer: {enhancer_id}"
        ]
        
        cmp_file = EVAL_RESULTS_DIR / instance_id / "comparison.json"
        if cmp_file.exists():
            print(f"Skipping {pred_file.name}, already evaluated.")
            continue
            
        print(f"\n[{pred_file.name}] Starting evaluation...")
        result = subprocess.run(cmd, env=os.environ.copy())
        if result.returncode != 0:
            print(f"Error evaluating {pred_file.name}")
            
if __name__ == "__main__":
    main()
